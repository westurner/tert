/// Shellwrap CLI binary: Execute shell commands with colored output streaming
/// 
/// Full implementation of the Shellwrap class in Rust with support for:
/// - Command execution with real-time streaming output
/// - Dual log files (plain-text and ANSI-colored)
/// - ANSI escape sequence stripping
/// - Color mode management (auto, always, never)
/// - Interactive shell execution
/// - Bash environment file injection
use std::env;
use std::fs::{self, File};
use std::io::{self, BufRead, BufReader, Write};
use std::process::{Command, Stdio};
use std::sync::{Arc, Mutex};
use std::thread;

/// Main Shellwrap implementation
struct Shellwrap {
    log_file: String,
    log_file_ansi: String,
    keep_ansi: bool,
    trace: bool,
    color_mode: String,  // auto, always, never
    shell: Option<String>,
    use_bash_env: bool,
    bash_env_file: String,
    interactive_shell: bool,
}

impl Shellwrap {
    /// Create a new Shellwrap instance with defaults
    fn new() -> Self {
        Shellwrap {
            log_file: "build.log".to_string(),
            log_file_ansi: "build.log.ansi".to_string(),
            keep_ansi: false,
            trace: true,
            color_mode: "auto".to_string(),
            shell: None,
            use_bash_env: false,
            bash_env_file: dirs_home()
                .map(|p| format!("{}/.shellwrap_aliases.sh", p))
                .unwrap_or_else(|| ".shellwrap_aliases.sh".to_string()),
            interactive_shell: false,
        }
    }

    /// Set log file path
    fn with_log_file(mut self, path: impl Into<String>) -> Self {
        self.log_file = path.into();
        self
    }

    /// Set ANSI log file path
    fn with_log_file_ansi(mut self, path: impl Into<String>) -> Self {
        self.log_file_ansi = path.into();
        self
    }

    /// Set whether to keep ANSI colors in log
    #[allow(dead_code)]
    fn with_keep_ansi(mut self, keep: bool) -> Self {
        self.keep_ansi = keep;
        self
    }

    /// Set trace mode
    fn with_trace(mut self, trace: bool) -> Self {
        self.trace = trace;
        self
    }

    /// Set color mode
    fn with_color_mode(mut self, mode: impl Into<String>) -> Self {
        self.color_mode = mode.into();
        self
    }

    /// Set shell to use
    fn with_shell(mut self, shell: impl Into<String>) -> Self {
        self.shell = Some(shell.into());
        self
    }

    /// Set whether to use interactive shell
    fn with_interactive_shell(mut self, interactive: bool) -> Self {
        self.interactive_shell = interactive;
        self
    }

    /// Strip ANSI escape sequences from text
    #[allow(dead_code)]
    fn strip_ansi(&self, text: &str) -> String {
        // Regex pattern: \x1b\[[0-9;]*[a-zA-Z]
        let mut result = String::new();
        let bytes = text.as_bytes();
        let mut i = 0;

        while i < bytes.len() {
            // Check for ANSI escape sequence: ESC [ ... letter
            if i + 2 < bytes.len()
                && bytes[i] == b'\x1b'
                && bytes[i + 1] == b'['
            {
                // Skip until we find a letter
                i += 2;
                while i < bytes.len() && !bytes[i].is_ascii_alphabetic() {
                    i += 1;
                }
                // Skip the letter
                if i < bytes.len() {
                    i += 1;
                }
            } else {
                result.push(bytes[i] as char);
                i += 1;
            }
        }

        result
    }

    /// Set environment variables for colored output
    fn set_color_env(&self) {
        #[cfg(unix)]
        let is_tty = unsafe { libc::isatty(1) } != 0;
        #[cfg(not(unix))]
        let is_tty = false;

        let use_colors = match self.color_mode.as_str() {
            "always" => true,
            "never" => false,
            "auto" => is_tty,
            _ => false,
        };

        if use_colors {
            env::set_var("CARGO_TERM_COLOR", "always");
            env::set_var("FORCE_COLOR", "1");
            // Only set PYTEST_ADDOPTS if not already set
            if env::var("PYTEST_ADDOPTS").is_err() {
                env::set_var("PYTEST_ADDOPTS", "--color=yes");
            }
            env::remove_var("NO_COLOR");
        } else {
            env::set_var("NO_COLOR", "1");
            env::remove_var("FORCE_COLOR");
            env::remove_var("CARGO_TERM_COLOR");
        }
    }

    /// Setup BASH_ENV file for alias injection
    fn setup_bash_env(&self) -> io::Result<()> {
        if !self.use_bash_env {
            return Ok(());
        }

        let content = "#!/bin/bash
# Auto-generated by shellwrap for color alias injection
shopt -s expand_aliases
alias ls='ls --color=always'
alias grep='grep --color=always'
alias diff='diff --color=always'
alias tree='tree -C'
";

        fs::write(&self.bash_env_file, content)?;

        // Set file permissions to 600 (rw-------)
        #[cfg(unix)]
        {
            use std::os::unix::fs::PermissionsExt;
            let permissions = std::fs::Permissions::from_mode(0o600);
            fs::set_permissions(&self.bash_env_file, permissions)?;
        }

        if self.trace {
            eprintln!(
                "[shellwrap] Created BASH_ENV at {} with permissions 600",
                self.bash_env_file
            );
        }

        Ok(())
    }

    /// Get the shell command and arguments
    fn get_shell_command(&self, cmd: &str) -> (String, Vec<String>) {
        let shell_name = self.shell.as_deref().unwrap_or("bash");
        
        let shell_exe = if shell_name.starts_with('/') {
            shell_name.to_string()
        } else {
            match shell_name {
                "bash" => "/bin/bash".to_string(),
                "zsh" => "/bin/zsh".to_string(),
                _ => which::which(shell_name)
                    .map(|p| p.to_string_lossy().to_string())
                    .unwrap_or_else(|_| shell_name.to_string()),
            }
        };

        let mut args = vec![shell_exe.clone()];
        if self.interactive_shell {
            args.push("-i".to_string());
        }
        args.push("-c".to_string());
        args.push(cmd.to_string());

        (shell_exe, args)
    }

    /// Execute a command with streaming output
    fn execute_streaming(&self, cmd: &str) -> io::Result<i32> {
        self.set_color_env();
        self.setup_bash_env()?;

        let (shell_exe, shell_args) = self.get_shell_command(cmd);

        if self.trace {
            eprintln!("[shellwrap] Executing: {} {:?}", shell_exe, shell_args);
        }

        let ansi_output = Arc::new(Mutex::new(String::new()));
        let ansi_output_clone = Arc::clone(&ansi_output);

        // Spawn the process
        let mut child = Command::new(&shell_exe)
            .args(&shell_args[1..])
            .stdout(Stdio::piped())
            .stderr(Stdio::piped())
            .spawn()?;

        let stdout = child.stdout.take().ok_or_else(|| {
            io::Error::other("Failed to capture stdout")
        })?;

        let stderr = child.stderr.take().ok_or_else(|| {
            io::Error::other("Failed to capture stderr")
        })?;

        // Thread to read stdout
        let stdout_handle = {
            let ansi_output_clone = Arc::clone(&ansi_output_clone);
            thread::spawn(move || {
                let reader = BufReader::new(stdout);
                for line in reader.lines().map_while(Result::ok) {
                    let line_with_newline = format!("{}\n", line);
                    print!("{}", line_with_newline);
                    let mut output = ansi_output_clone.lock().unwrap();
                    output.push_str(&line_with_newline);
                }
            })
        };

        // Thread to read stderr (combined with stdout)
        let stderr_handle = {
            let ansi_output_clone = Arc::clone(&ansi_output_clone);
            thread::spawn(move || {
                let reader = BufReader::new(stderr);
                for line in reader.lines().map_while(Result::ok) {
                    let line_with_newline = format!("{}\n", line);
                    eprint!("{}", line_with_newline);
                    let mut output = ansi_output_clone.lock().unwrap();
                    output.push_str(&line_with_newline);
                }
            })
        };

        // Wait for threads to complete
        let _ = stdout_handle.join();
        let _ = stderr_handle.join();

        // Get exit code
        let exit_code = child.wait()?.code().unwrap_or(1);

        // Write logs with captured output
        let output = ansi_output.lock().unwrap().clone();
        self.write_logs(&output)?;

        Ok(exit_code)
    }

    /// Write log files (plain and ANSI)
    fn write_logs(&self, output: &str) -> io::Result<()> {
        // Write ANSI log
        let mut ansi_file = File::create(&self.log_file_ansi)?;
        ansi_file.write_all(output.as_bytes())?;

        // Write plain log (strip ANSI codes)
        let plain_output = self.strip_ansi(output);
        let mut plain_file = File::create(&self.log_file)?;
        plain_file.write_all(plain_output.as_bytes())?;

        // Remove ANSI log if not keeping it
        if !self.keep_ansi {
            let _ = fs::remove_file(&self.log_file_ansi);
        }

        Ok(())
    }

    /// Print color palette to stdout
    fn print_colors(&self) {
        println!("Standard Colors:");
        let colors = [("BLACK", 30),
            ("RED", 31),
            ("GREEN", 32),
            ("YELLOW", 33),
            ("BLUE", 34),
            ("MAGENTA", 35),
            ("CYAN", 36),
            ("WHITE", 37)];

        for (name, code) in colors.iter() {
            println!("\x1b[{}m{:<12} (code: {})\x1b[0m", code, name, code);
        }

        println!("\nBright Colors:");
        for (name, code) in colors.iter() {
            println!("\x1b[1;{}m{:<12} (bright, code: 1;{})\x1b[0m", code, name, code);
        }

        println!("\n256-Color Palette:");
        for i in 0..256 {
            if i < 16 {
                print!("\x1b[38;5;{}m ▌ \x1b[0m", i);
            } else {
                print!("\x1b[48;5;{}m   \x1b[0m", i);
            }
            if (i + 1) % 16 == 0 {
                println!();
            }
        }
    }
}

/// Get home directory
fn dirs_home() -> Option<String> {
    env::var("HOME")
        .ok()
        .or_else(|| env::var("USERPROFILE").ok())
}

fn main() {
    let args: Vec<String> = env::args().collect();

    if args.len() < 2 {
        print_usage(&args[0]);
        std::process::exit(1);
    }

    let mut shellwrap = Shellwrap::new();
    let mut skip_next = false;
    let mut command_parts = Vec::new();

    // Parse arguments
    for (i, arg) in args.iter().enumerate().skip(1) {
        if skip_next {
            skip_next = false;
            continue;
        }

        match arg.as_str() {
            "--help" | "-h" => {
                print_usage(&args[0]);
                std::process::exit(0);
            }
            "--colors" | "--print-colors" => {
                shellwrap.print_colors();
                std::process::exit(0);
            }
            "--version" | "-v" => {
                println!("shellwrap 0.1.0");
                std::process::exit(0);
            }
            "--log" => {
                if i + 1 < args.len() {
                    shellwrap = shellwrap.with_log_file(args[i + 1].clone());
                    skip_next = true;
                }
            }
            "--log-ansi" => {
                if i + 1 < args.len() {
                    shellwrap = shellwrap.with_log_file_ansi(args[i + 1].clone());
                    skip_next = true;
                }
            }
            "--no-log" => {
                shellwrap = shellwrap.with_log_file("/dev/null");
            }
            "--color" | "--color-mode" => {
                if i + 1 < args.len() {
                    shellwrap = shellwrap.with_color_mode(args[i + 1].clone());
                    skip_next = true;
                }
            }
            "--trace" | "-t" => {
                shellwrap = shellwrap.with_trace(true);
            }
            "--no-trace" => {
                shellwrap = shellwrap.with_trace(false);
            }
            "--shell" => {
                if i + 1 < args.len() {
                    shellwrap = shellwrap.with_shell(args[i + 1].clone());
                    skip_next = true;
                }
            }
            "--interactive" | "-i" => {
                shellwrap = shellwrap.with_interactive_shell(true);
            }
            "--test" => {
                shellwrap.print_colors();
                std::process::exit(0);
            }
            _ if arg.starts_with('-') => {
                eprintln!("Unknown option: {}", arg);
                std::process::exit(1);
            }
            _ => {
                command_parts.push(arg.clone());
            }
        }
    }

    if command_parts.is_empty() {
        eprintln!("Error: No command specified");
        print_usage(&args[0]);
        std::process::exit(1);
    }

    let command = command_parts.join(" ");

    // Execute command
    match shellwrap.execute_streaming(&command) {
        Ok(exit_code) => {
            std::process::exit(exit_code);
        }
        Err(e) => {
            eprintln!("Error executing command: {}", e);
            std::process::exit(1);
        }
    }
}

fn print_usage(prog: &str) {
    eprintln!("Usage: {} [OPTIONS] COMMAND", prog);
    eprintln!();
    eprintln!("OPTIONS:");
    eprintln!("  --help                  Show this help message");
    eprintln!("  --version               Show version");
    eprintln!("  --colors                Print color palette and exit");
    eprintln!("  --log FILE              Set plain-text log file");
    eprintln!("  --log-ansi FILE         Set ANSI log file");
    eprintln!("  --no-log                Disable logging");
    eprintln!("  --color MODE            Set color mode (auto, always, never)");
    eprintln!("  --trace                 Enable trace output");
    eprintln!("  --no-trace              Disable trace output");
    eprintln!("  --shell SHELL           Shell to use (bash, zsh, or path)");
    eprintln!("  --interactive           Use interactive shell (-i flag)");
    eprintln!("  --test                  Run in test mode (print colors)");
    eprintln!();
    eprintln!("EXAMPLES:");
    eprintln!("  {} \"echo hello\"", prog);
    eprintln!("  {} --colors", prog);
    eprintln!("  {} --log build.log --log-ansi build.log.ansi \"cargo test\"", prog);
}
